def larger(num1, num2):
    """
    num1 and num2 are two numbers.
    Returns the larger of the two numbers.
    """
    if num1 > num2:
        return num1
    else:
        return num2
    
# call the larger function with values 3 and 5
# store the result in the variable called result
# then print the result
result = larger(3, 5)
print(result)

